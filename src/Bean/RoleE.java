package Bean;

public class RoleE {
	private String role;
	private String name;
	
	public RoleE(String role, String name) {
		super();
		this.role = role;
		this.name = name;
	}
	public RoleE() {
		super();
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	

}
